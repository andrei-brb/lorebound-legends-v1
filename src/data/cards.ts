import { inferElementFromTags, type Element } from "@/lib/elementSystem";
import type { CardGameRules, WeaponGameRules } from "./cardGameRules";
import type { BattleKeywordId } from "@/lib/keywords";

export type Rarity = "legendary" | "rare" | "common";
export type CardType = "hero" | "god" | "weapon" | "spell" | "trap";

export interface CardAbility {
  name: string;
  description: string;
  cost?: number;
  cooldown?: number; // turns between uses, undefined = 1 use per battle
}

export interface Synergy {
  partnerId: string;
  name: string;
  description: string;
  boostedStat: string;
  boostValue: number;
}

export interface WeaponBonus {
  attack: number;
  defense: number;
}

export interface SpellEffect {
  type: "damage" | "heal" | "buff_attack" | "buff_defense" | "debuff_attack" | "debuff_defense";
  value: number;
  target: "single_enemy" | "all_enemies" | "single_ally" | "all_allies" | "self";
  duration?: number; // turns for buffs/debuffs
}

export interface TrapEffect {
  trigger: "on_attacked" | "on_enemy_play" | "on_enemy_ability";
  effect: "reflect_damage" | "redirect" | "stun" | "damage";
  value: number;
}

export interface GameCard {
  id: string;
  name: string;
  type: CardType;
  rarity: Rarity;
  image: string;
  attack: number;
  defense: number;
  hp: number; // field HP for heroes/gods; 0 for weapons/spells/traps
  tags: string[]; // for synergy matching e.g. ["warrior", "olympus"]
  specialAbility: CardAbility;
  lore: string;
  synergies: Synergy[];
  level: number;
  xp: number;
  xpToNext: number;
  loreArc?: string;
  // Weapon-specific
  weaponBonus?: WeaponBonus;
  // Spell-specific
  spellEffect?: SpellEffect;
  // Trap-specific
  trapEffect?: TrapEffect;
  // Passive ability (always active on field)
  passiveAbility?: { name: string; description: string; stat: "attack" | "defense"; value: number; targetTag?: string };
  // Element (auto-inferred from tags if not set)
  element?: Element;
  // Seasonal flag
  seasonal?: boolean;
  /** Machine-readable ability / weapon hooks (see cardGameRules.ts) */
  cardRules?: CardGameRules;
  weaponRules?: WeaponGameRules;
  /** Combat keywords (Taunt, Lifesteal, Poison) when on field */
  keywords?: BattleKeywordId[];
}

// Helper to add element to card arrays
function withElements<T extends GameCard[]>(cards: T): T {
  for (const card of cards) {
    if (!card.element) card.element = inferElementFromTags(card.tags);
  }
  return cards;
}

